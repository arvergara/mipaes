// Función para obtener preguntas según la materia
export function getQuestionsBySubject(subject: Subject): Question[] {
  switch (subject) {
    case 'M1':
      return m1Questions;
    case 'M2':
      return m2Questions;
    case 'L':
      return lQuestions;
    case 'C':
      return cQuestions;
    case 'H':
      return hQuestions;
    case 'ALL':
      return [...m1Questions, ...m2Questions, ...lQuestions, ...cQuestions, ...hQuestions];
    default:
      return [];
  }
}